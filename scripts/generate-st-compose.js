#!/usr/bin/env node
/**
 * generate-st-compose.js
 *
 * Reads active bots from PostgreSQL bot_accounts table and generates
 * docker-compose.st.yml with one SillyTavern service per bot.
 *
 * Usage: node scripts/generate-st-compose.js
 *
 * Each bot gets:
 *   - Container: weclaw-st-{safe_bot_id}
 *   - Port:      127.0.0.1:{8001+bot_index}:8000
 *   - Volume:    ./st-data/{safe_bot_id}:/home/node/app/data
 *   - Network:   companion-net (external, name: weclaw-companion-net)
 */

const fs = require('fs');
const path = require('path');
const { Pool } = require('pg');

const DATABASE_URL = process.env.DATABASE_URL
  || 'postgresql://weclaw:weclaw_secret@postgres:5432/weclaw_companion';

const COMPOSE_PATH = path.resolve(__dirname, '..', 'docker-compose.st.yml');

const PORT_BASE = 8001;

/**
 * Convert raw bot_id to a safe filesystem/container name:
 * replace @ and . with -
 */
function safeBotId(botId) {
  return botId.replace(/[@.]/g, '-');
}

async function main() {
  const pool = new Pool({ connectionString: DATABASE_URL });

  try {
    // 1. Query active bots (not deleted) ordered by bot_index
    const result = await pool.query(
      `SELECT b.bot_id, b.bot_index, b.character_id, b.wechat_id,
              ct.name AS char_name
       FROM bot_accounts b
       LEFT JOIN character_templates ct ON b.character_id = ct.id
       WHERE b.is_active = TRUE AND b.deleted_at IS NULL
       ORDER BY b.bot_index`
    );

    const bots = result.rows;
    const botCount = bots.length;

    // 2. Build compose YAML
    const timestamp = new Date().toISOString();
    const lines = [];

    lines.push('# =============================================================================');
    lines.push(`# SillyTavern Docker Compose - Auto-generated by generate-st-compose.js`);
    lines.push(`# Generated at: ${timestamp}`);
    lines.push(`# Active bots: ${botCount}`);
    lines.push('# =============================================================================');
    lines.push('');
    lines.push('services:');

    if (botCount === 0) {
      lines.push('  # No active bots found');
      lines.push('  {}');
    } else {
      for (const bot of bots) {
        const sid = safeBotId(bot.bot_id);
        const port = PORT_BASE + bot.bot_index;
        const charStr = bot.char_name || bot.character_id || 'none';

        lines.push(`  st-${sid}:`);
        lines.push(`    image: ghcr.io/sillytavern/sillytavern:latest`);
        lines.push(`    container_name: weclaw-st-${sid}`);
        lines.push(`    ports:`);
        lines.push(`      - "127.0.0.1:${port}:8000"`);
        lines.push(`    volumes:`);
        lines.push(`      - ./st-data/${sid}/data:/home/node/app/data`);
        lines.push(`      - ./st-data/${sid}/config:/home/node/app/config`);
        lines.push(`    environment:`);
        lines.push(`      - STORAGE_PATH=/home/node/app/data`);
        lines.push(`    labels:`);
        lines.push(`      - "com.weclaw.bot_id=${bot.bot_id}"`);
        lines.push(`      - "com.weclaw.wechat_id=${bot.wechat_id || ''}"`);
        lines.push(`      - "com.weclaw.character=${charStr}"`);
        lines.push(`      - "com.weclaw.bot_index=${bot.bot_index}"`);
        lines.push(`    restart: unless-stopped`);
        lines.push(`    networks:`);
        lines.push(`      - companion-net`);
        lines.push('');
      }
    }

    lines.push('networks:');
    lines.push('  companion-net:');
    lines.push('    external: true');
    lines.push('    name: weclaw-companion-net');
    lines.push('');

    const yaml = lines.join('\n');

    // 3. Write to file
    fs.writeFileSync(COMPOSE_PATH, yaml, 'utf-8');
    console.log(`[generate-st-compose] ✅ Wrote ${COMPOSE_PATH}`);
    console.log(`[generate-st-compose] 📊 ${botCount} active bot(s)`);
    for (const bot of bots) {
      const sid = safeBotId(bot.bot_id);
      const port = PORT_BASE + bot.bot_index;
      console.log(`   - ${bot.bot_id} → st-${sid} :${port} ${bot.char_name ? '('+bot.char_name+')' : ''}`);
    }
  } catch (err) {
    console.error(`[generate-st-compose] ❌ Failed:`, err.message);
    process.exit(1);
  } finally {
    await pool.end();
  }
}

main();
